def text2json(dtype='train', ytype='2020'):
    disease2id = {}
    id2disease = {}
    with open('data/' + dtype + '/label.txt', 'r') as o:
        lines = o.readlines()

    count = 0
    for line in lines:
        id, _, answer = line.strip().split('|')

        if answer.lower() == 'no' or answer.lower() == 'yes':
            continue

        if answer not in disease2id.keys():
            disease2id[answer] = count
            id2disease[count] = answer
            count += 1

    with open('data/disease2id.json', 'w') as f:
        json.dump(disease2id, f)

    with open('data/id2disease.json', 'w') as f:
        json.dump(id2disease, f)


